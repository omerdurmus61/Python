#Sting özellikleri

isim= "omer"

#capitalize() özelliği stringin ilk harfini büyük harf yapar.
isim = isim.capitalize()

print(isim)

tamIsim="omer can durmus"

#split() özelliği boşluk karakterine göre cümleyi kelimelere ayırır.
print(tamIsim.split())

#upper özelliği bütün karakterleri büyük yapar.
print(tamIsim.upper())
